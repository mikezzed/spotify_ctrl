/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_RTC_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_USART1_UART_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */

  // Start PWM channels for LEDs
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1); // PA0
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2); // PA1
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3); // PA2
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_4); // PA3
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1); // PA6

  uint16_t fade_counter = 0;

  /* USER CODE END 2 */
  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
    // Check all pushbuttons
    if (HAL_GPIO_ReadPin(BTN_PORT, BTN_PLAY_PAUSE))
        SendMediaKey(HID_PLAY_PAUSE);
    if (HAL_GPIO_ReadPin(BTN_PORT, BTN_NEXT))
        SendMediaKey(HID_NEXT);
    if (HAL_GPIO_ReadPin(BTN_PORT, BTN_PREV))
        SendMediaKey(HID_PREV);
    if (HAL_GPIO_ReadPin(BTN_PORT, BTN_VOL_UP))
        SendMediaKey(HID_VOL_UP);
    if (HAL_GPIO_ReadPin(BTN_PORT, BTN_VOL_DOWN))
        SendMediaKey(HID_VOL_DOWN);

    
    // TODO: Move LED pattern handling into separate function.
	  for (int i = 0; i < NUM_LEDS; i++)
	      {
	          // Calculate LED's individual phase-shifted fade step
	          uint16_t phase = (fade_counter + i * PHASE_SHIFT) % (2 * MAX_DUTY);
	          uint16_t duty;

	          if (phase <= MAX_DUTY)
	              duty = phase;
	          else
	              duty = 2 * MAX_DUTY - phase;

	          // Apply to corresponding PWM channel
	          switch (i)
	          {
	              case 0: __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, duty); break; // PA0
	              case 1: __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, duty); break; // PA1
	              case 2: __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, duty); break; // PA2
	              case 3: __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_4, duty); break; // PA3
	              case 4: __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, duty); break; // PA6
	          }
	      }

	      fade_counter = (fade_counter + 1) % (2 * MAX_DUTY); // Loop the counter
	      HAL_Delay(STEP_DELAY_MS); // Controls wave speed
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

void SendMediaKey(uint16_t keycode)
{
    uint8_t report[2];
    report[0] = keycode & 0xFF;
    report[1] = (keycode >> 8) & 0xFF;

    USBD_HID_SendReport(&hUsbDeviceFS, report, sizeof(report));
    HAL_Delay(10); // Small delay
    uint8_t release[2] = {0, 0};
    USBD_HID_SendReport(&hUsbDeviceFS, release, sizeof(release));
}

